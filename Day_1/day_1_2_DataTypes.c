// Data types in C

#include <stdio.h>

int main(){
// int
int myInt = 12;

float myFloat = 12.22; //float

char myChar = 'c'; // char 

double myDouble = 56.4545454;

//printing data types
printf("%i \n", myInt);
printf("%f \n", myFloat);
printf("%c \n", myChar);
printf("%lf \n", myDouble);


// format specifiers ( in double quotes) 
// %d or %i - integer
// %f or %F - float
// %c - char
// %lf - double
// %s - String

}