// Switch Statements in C
#include<stdio.h>
int main(){
int day = 4;

switch(day){

case 1:
printf("MON");
break;

case 2:
printf("TUE");
break;

case 3:
printf("WED");
break;

case 4:
printf("THUS");
break;

case 5:
printf("FRI");
break;

default:
printf("SAT & SUN is Holiday");
break;
}

}
